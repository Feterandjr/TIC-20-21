'''Escriba una función llamada “multiplicación”
que reciba como argumento cuatro números reales
distintos de cero y que devuelva su producto.'''
def multiplicacion():
    first=int(input("First Number:"))
    second=int(input("Second Number:"))
    third=int(input("Third Number:"))
    fourth=int(input("Fourth Number:"))
    print ("Answer is",first*second*third*fourth)

multiplicacion()
